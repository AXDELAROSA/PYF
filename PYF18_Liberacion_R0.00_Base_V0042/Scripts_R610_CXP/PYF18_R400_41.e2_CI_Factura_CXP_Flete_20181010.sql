	EXECUTE [dbo].[PG_UP_FACTURA_CXP_K_PROVEEDOR]		@PP_L_DEBUG, @PP_K_SISTEMA_EXE, @PP_K_USUARIO_ACCION,
														@PP_K_FACTURA_CXP
	-- ========================================
GO





