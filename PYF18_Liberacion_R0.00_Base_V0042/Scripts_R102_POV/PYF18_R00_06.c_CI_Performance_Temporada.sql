													1, -- @PP_K_DATA_SISTEMA,	
													@OU_RESULTADO_VALIDACION = @VP_MENSAJE		OUTPUT
	-- ///////////////////////////////////////////

	IF @VP_MENSAJE=''
		BEGIN

		DECLARE @VP_VALOR_ACUMULADO			DECIMAL=0
		DECLARE @VP_V01_VALOR				DECIMAL=0
		DECLARE @VP_V02_VALOR				DECIMAL=0
		
		DECLARE @VP_XLS_UO					DECIMAL=0
