		SELECT @VP_XLS_UO	=	XLS_UO,	@VP_XLS_UNIDAD_OPERATIVA=XLS_UNIDAD_OPERATIVA
								FROM PERFORMANCE_N3_X_ME
								WHERE	K_YYYY=@PP_K_YYYY
								AND		K_UNIDAD_OPERATIVA=@PP_K_UNIDAD_OPERATIVA

		IF @PP_K_TEMPORADA=1
		BEGIN			
		
			SELECT @VP_V01_VALOR	=	M10_VALOR + M11_VALOR + M12_VALOR 
									FROM PERFORMANCE_N3_X_ME
									WHERE	K_YYYY=@PP_K_YYYY-1
									AND		K_UNIDAD_OPERATIVA=@PP_K_UNIDAD_OPERATIVA

			SELECT @VP_V01_VALOR	=	@VP_V01_VALOR + M01_VALOR + M02_VALOR + M03_VALOR 
									FROM PERFORMANCE_N3_X_ME
									WHERE	K_YYYY=@PP_K_YYYY
									AND		K_UNIDAD_OPERATIVA=@PP_K_UNIDAD_OPERATIVA

			SELECT @VP_V02_VALOR	=	M04_VALOR + M05_VALOR + M06_VALOR + M07_VALOR + M08_VALOR + M09_VALOR
									FROM PERFORMANCE_N3_X_ME
									WHERE	K_YYYY=@PP_K_YYYY
									AND		K_UNIDAD_OPERATIVA=@PP_K_UNIDAD_OPERATIVA

			

		END
		IF @PP_K_TEMPORADA=2
		BEGIN
			SELECT @VP_V01_VALOR	=	M04_VALOR + M05_VALOR + M06_VALOR + M07_VALOR + M08_VALOR + M09_VALOR
									FROM PERFORMANCE_N3_X_ME
									WHERE	K_YYYY=@PP_K_YYYY
									AND		K_UNIDAD_OPERATIVA=@PP_K_UNIDAD_OPERATIVA
		
			SELECT @VP_V02_VALOR	=	M10_VALOR + M11_VALOR + M12_VALOR 
									FROM PERFORMANCE_N3_X_ME
									WHERE	K_YYYY=@PP_K_YYYY
									AND		K_UNIDAD_OPERATIVA=@PP_K_UNIDAD_OPERATIVA

			SELECT @VP_V02_VALOR	=	@VP_V02_VALOR + M01_VALOR + M02_VALOR + M03_VALOR 
									FROM PERFORMANCE_N3_X_ME
									WHERE	K_YYYY=@PP_K_YYYY+1
									AND		K_UNIDAD_OPERATIVA=@PP_K_UNIDAD_OPERATIVA
		END

		SET @VP_VALOR_ACUMULADO= @VP_V01_VALOR + @VP_V02_VALOR

		DECLARE @VP_P01_VALOR				DECIMAL(5,2)=0
		DECLARE @VP_P02_VALOR				DECIMAL(5,2)=0

		IF @VP_VALOR_ACUMULADO>0
		BEGIN
			SET @VP_P01_VALOR=(@VP_V01_VALOR/@VP_VALOR_ACUMULADO)*100
			SET @VP_P02_VALOR=(@VP_V02_VALOR/@VP_VALOR_ACUMULADO)*100
		END
		

		IF @VP_VALOR_ACUMULADO IS NULL
			SET @VP_VALOR_ACUMULADO=0
		IF @VP_V01_VALOR IS NULL
			SET @VP_V01_VALOR=0
		IF @VP_V02_VALOR IS NULL
			SET @VP_V02_VALOR=0
		IF @VP_P01_VALOR IS NULL
			SET @VP_P01_VALOR=0
		IF @VP_P02_VALOR IS NULL
			SET @VP_P02_VALOR=0
		
		--============================================================================================

		EXECUTE [PG_IN_PERFORMANCE_N3_X_TEMP]	@PP_L_DEBUG,@PP_K_SISTEMA_EXE,@PP_K_USUARIO_ACCION,
												@PP_K_YYYY,	@VP_XLS_UO, @VP_XLS_UNIDAD_OPERATIVA,
												@PP_K_UNIDAD_OPERATIVA,@PP_K_TEMPORADA,1,
												@VP_VALOR_ACUMULADO,0,@VP_V01_VALOR,@VP_V02_VALOR,
												@VP_P01_VALOR,@VP_P02_VALOR

		--============================================================================================
										
	END

	-- ////////////////////////////////////////////////


	-- ////////////////////////////////////////////////
GO


	


	
